def _response_code_guide(schema: dict[str, Any]) -> list[dict[str, Any]]:
    counts: Counter[str] = Counter()
    for _path, _method, operation in _iter_operations(schema):
        for code in operation.get("responses", {}):
            if isinstance(code, str) and code.isdigit():
                counts[code] += 1

    descriptions = {
        "200": "Standard success response with a body.",
        "201": "Resource created successfully.",
        "202": "Accepted for background or deferred processing.",
        "204": "Success with no response body.",
        "400": "Client-side request shape or domain precondition error.",
        "401": "Missing or invalid authentication.",
        "403": "Authenticated but rejected by role or access rules.",
        "404": "Resource or linked record not found.",
        "409": "State conflict on selected routes.",
        "422": "Validation error generated by FastAPI/Pydantic.",
        "500": "Internal server error fallback.",
    }

    preferred_order = ["200", "201", "202", "204", "400", "401", "403", "404", "409", "422", "500"]
    result: list[dict[str, Any]] = []
    for code in preferred_order:
        if counts.get(code):
            result.append(
                {
                    "code": code,
                    "operations": counts[code],
                    "description": descriptions.get(code, "Route-specific response code."),
                }
            )
    return result


def _auth_route_families(schema: dict[str, Any]) -> list[dict[str, Any]]:
    families: dict[str, list[str]] = defaultdict(list)
    for path in sorted(schema.get("paths", {})):
        if path.startswith("/api/v1/authentication"):
            families["authentication"].append(path)
        elif path.startswith("/api/v1/auth"):
            families["auth"].append(path)
        elif path.startswith("/api/v1/2fa"):
            families["two-factor"].append(path)
        elif path.startswith("/api/v1/phone-verification"):
            families["phone-verification"].append(path)
        elif path.startswith("/api/v1/password-reset"):
            families["password-reset"].append(path)

    return [
        {"family": family, "sample_paths": paths[:8], "path_count": len(paths)}
        for family, paths in sorted(families.items())
    ]


@router.get("/documentation/endpoints")
async def get_detailed_endpoints_documentation(
    request: Request,
    category: Optional[str] = Query(None, description="Tag/category slug from the generated schema"),
    current_user: User = Depends(get_current_user),
):
    """Return a generated endpoint index derived from the current OpenAPI schema."""
    schema = request.app.openapi()
    categories = _operation_index(schema)

    if category:
        slug = _slugify_tag(category)
        if slug not in categories:
            raise HTTPException(status_code=404, detail=f"Категория '{category}' не найдена")
        return {slug: categories[slug]}

    return {
        "kind": "generated-endpoint-index",
        "canonical_sources": {
            "swagger_ui": "/docs",
            "redoc": "/redoc",
            "openapi_json": "/openapi.json",
        },
        "category_count": len(categories),
        "categories": list(categories.values()),
        "reader_note": "This endpoint is a generated helper index. The canonical contract still lives in /docs and /openapi.json.",
    }


@router.get("/documentation/examples")
async def get_api_examples(
    endpoint: Optional[str] = Query(None, description="Example group key"),
    current_user: User = Depends(get_current_user),
):
    """Return curated live examples for selected route families."""
    examples = _example_catalog()

    if endpoint:
        if endpoint not in examples:
            raise HTTPException(
                status_code=404,
                detail=f"Примеры для эндпоинта '{endpoint}' не найдены",
            )
        return {endpoint: examples[endpoint]}

    return {
        "kind": "curated-live-examples",
        "canonical_sources": {
            "swagger_ui": "/docs",
            "openapi_json": "/openapi.json",
        },
        "examples": examples,
        "reader_note": "Examples are intentionally small and only cover selected live flows.",
    }



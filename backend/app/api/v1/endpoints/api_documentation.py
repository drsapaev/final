@router.get("/documentation/authentication")
async def get_authentication_documentation(request: Request):
    """Return a live authentication guide based on current OpenAPI metadata."""
    schema = request.app.openapi()
    security_schemes = schema.get("components", {}).get("securitySchemes", {})
    token_urls = []
    for scheme in security_schemes.values():
        flows = scheme.get("flows", {})
        password_flow = flows.get("password")
        if password_flow and password_flow.get("tokenUrl"):
            token_urls.append(password_flow["tokenUrl"])

    return {
        "kind": "generated-auth-guide",
        "overview": "The API uses Bearer-token authentication. Canonical auth details come from the generated OpenAPI security schemes.",
        "security_schemes": security_schemes,
        "token_urls": token_urls,
        "route_families": _auth_route_families(schema),
        "notes": [
            "The /api/v1/auth/* and /api/v1/authentication/* surfaces are both live and serve different purposes.",
            "Two-factor, phone verification, and password reset routes are adjacent auth-related families, not one single login flow.",
            "Use /docs or /openapi.json when a consumer needs exact request/response details.",
        ],
    }

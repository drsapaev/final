router = APIRouter()

_HTTP_METHODS = {"get", "post", "put", "patch", "delete", "options", "head"}


def _iter_operations(schema: dict[str, Any]):
    for path, path_item in schema.get("paths", {}).items():
        for method, operation in path_item.items():
            if method.lower() in _HTTP_METHODS:
                yield path, method.lower(), operation


def _slugify_tag(tag: str) -> str:
    return tag.strip().lower().replace(" ", "-").replace("_", "-")


def _operation_index(schema: dict[str, Any]) -> dict[str, dict[str, Any]]:
    buckets: dict[str, dict[str, Any]] = {}

    for path, method, operation in _iter_operations(schema):
        tags = operation.get("tags") or ["untagged"]
        for raw_tag in tags:
            slug = _slugify_tag(raw_tag)
            bucket = buckets.setdefault(
                slug,
                {
                    "tag": raw_tag,
                    "slug": slug,
                    "description": "Generated from the current OpenAPI schema.",
                    "operation_count": 0,
                    "methods": set(),
                    "sample_operations": [],
                },
            )
            bucket["operation_count"] += 1
            bucket["methods"].add(method.upper())
            if len(bucket["sample_operations"]) < 5:
                bucket["sample_operations"].append(
                    {
                        "method": method.upper(),
                        "path": path,
                        "summary": operation.get("summary"),
                    }
                )

    for bucket in buckets.values():
        bucket["methods"] = sorted(bucket["methods"])

    return dict(sorted(buckets.items(), key=lambda item: (-item[1]["operation_count"], item[0])))


